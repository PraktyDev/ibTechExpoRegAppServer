import mongoose from "mongoose";

const volunteerSchema = mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        required: true,
    },
},{ timestamps: true });

const Volunteer = mongoose.model("Volunteer", volunteerSchema);

export default Volunteer;